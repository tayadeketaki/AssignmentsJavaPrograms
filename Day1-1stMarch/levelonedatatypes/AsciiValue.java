package levelonedatatypes;

public class AsciiValue {
    public static void main(String[] args) {
        //Print the ASCII value of the character 'h'.
        char ch = 'h';
        int ascii = ch;
        System.out.println("The ASCII value for 'h' is : "+ascii);
        System.out.println("The ASCII value with int for 'h' is : "+ (int)ch);
    }
}
